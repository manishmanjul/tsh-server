package com.tsh.library.dto;

public class SimpleIntegerRequest {

	private int val;

	public SimpleIntegerRequest() {
		super();
	}

	public SimpleIntegerRequest(int val) {
		super();
		this.val = val;
	}

	public int getVal() {
		return val;
	}

	public void setVal(int val) {
		this.val = val;
	}
}
